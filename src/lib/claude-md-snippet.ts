/**
 * The actual "baked into Claude" mechanism — not a hook, not magic. Claude
 * Code reads CLAUDE.md automatically at the start of every session and
 * follows it as standing instructions. So instead of expecting a human to
 * remember to type `claude-code-merge-queue land` after every change, this snippet tells
 * the agent itself to do it — the same way a real production setup that
 * runs several parallel Claude Code agents keeps its own human out of the
 * loop for anything except the final promotion to production.
 *
 * The snippet is a MARKER..END_MARKER delimited block. `claude-code-merge-queue init`
 * writes it into CLAUDE.md (creating the file if missing, appending once if a
 * CLAUDE.md exists without the block) and — because the block is delimited at
 * both ends — regenerates it *in place* on any later run, so the workflow the
 * agent reads always matches the installed version with zero manual re-sync.
 * The block carries an explicit do-not-hand-edit notice: edits inside the
 * markers are overwritten on the next init; a human's own instructions belong
 * outside the markers, where nothing touches them.
 */
import { existsSync, readFileSync, writeFileSync, rmSync } from "node:fs";
import { join } from "node:path";
import type { ClaudeCodeMergeQueueConfig } from "./config.js";

export const MARKER = "<!-- claude-code-merge-queue:workflow -->";

/**
 * Opening of the closing marker. Detection keys off this prefix so the
 * human-readable tail (which "confirms close" and states the block is safe to
 * regenerate) can evolve without breaking the delimiter. The generator emits
 * the full END_MARKER line; detectors match on END_MARKER_PREFIX.
 */
export const END_MARKER_PREFIX = "<!-- claude-code-merge-queue:workflow:end";
export const END_MARKER = `${END_MARKER_PREFIX} — end of the managed block. \`claude-code-merge-queue init\` regenerates everything above (back to the opening marker) in place, so re-syncing is always safe; anything below this marker is yours and is never touched. -->`;

export function claudeMdSnippet(cfg: ClaudeCodeMergeQueueConfig): string {
  const promoteBlock = cfg.productionBranch
    ? `- **\`${cfg.productionBranch}\` is production, and it is not your call.** Only a human runs \`claude-code-merge-queue promote\` (${cfg.integrationBranch} → ${cfg.productionBranch}), on their own schedule. Never run it yourself, never suggest running it as part of "finishing" a task — landing on \`${cfg.integrationBranch}\` is the finish line for you.`
    : `- There's no separate production branch configured — \`${cfg.integrationBranch}\` already is it. Landing is the whole deal.`;

  return `${MARKER}
## Claude Code Merge Queue workflow

> ⚠️ **Do not hand-edit this section — it is generated and owned by Claude Code Merge Queue.** Everything between the two \`claude-code-merge-queue:workflow\` markers is regenerated by \`claude-code-merge-queue init\` on every sync, so any edits inside are overwritten. To change the workflow, edit the snippet in the package and re-run init; keep your own instructions *outside* the markers.

This repo uses [Claude Code Merge Queue](https://github.com/funador/claude-code-merge-queue) to run parallel Claude Code agents without them racing each other. It's not a suggestion — follow it by default, every session, without being asked:

- **You're probably already in a lane.** If you were started with \`claude --worktree <name>\`, Claude Code Merge Queue's \`WorktreeCreate\` hook already isolated you into your own git worktree and branch. Just work.
- **Commit before you land — always.** \`claude-code-merge-queue land\` pushes *committed* work, not your working tree: it rebases your branch and pushes it. Green checks are not enough — a lane with uncommitted changes is rejected up front (\`land\` refuses before it even joins the queue). So the sequence is \`git add -A && git commit\` (or several clean commits), *then* land. Running \`land\` on a dirty tree is the most common self-inflicted failure; don't.
- **Land the moment your change is green — don't ask first.** Once tests/build/lint pass and your work is committed, run \`claude-code-merge-queue land\` yourself. This is pre-authorized, standard workflow, not a decision that needs sign-off. It rebases onto \`${cfg.integrationBranch}\`, pushes through the FIFO landing queue, and syncs the dev checkout — that whole sequence is "done," not "pushed to a branch and waiting."
- **Never push \`${cfg.integrationBranch}\` directly.** \`git push origin HEAD:${cfg.integrationBranch}\` is rejected by the pre-push hook anyway — always go through \`claude-code-merge-queue land\`.
- **A failed \`claude-code-merge-queue land\` is not a stopping point — landed is the only "done."** A rebase conflict aborts safely and leaves your working tree clean; that's expected, not an error to report and hand back. Resolve the conflicting diff yourself the same way you'd fix any other bug, \`git add\` the result, \`git rebase --continue\`, then re-run \`claude-code-merge-queue land\` — repeat until it actually lands. Don't say a change is finished, complete, or ready while it's still sitting unlanded in your lane; \`checkCommand\` still gates the real push regardless of how many attempts it took, so a wrong resolution gets caught there, not by a human reading it first.
- **Use \`claude-code-merge-queue build-lock -- <cmd>\` for anything heavy** (a full build, a slow test suite) so parallel lanes take turns instead of thrashing the machine together.
- **A surfaced orphaned lane is a question for the human — not scrollback to skip past.** After a landing (and on demand via \`claude-code-merge-queue reconcile\`) you may see a \`⚠\` sibling lane flagged with unlanded commits or uncommitted work and no active session. That's stranded work with nobody at the keyboard: don't ignore it, and never quietly delete it. Surface it to the human and ask what to do — finish landing it, or discard it — their call, not yours. Run \`claude-code-merge-queue reconcile\` anytime to list stranded lanes; it only reports, never touches them.
${promoteBlock}

${END_MARKER}
`;
}

export type RemoveSnippetResult = "removed" | "not-found" | "mismatch" | "no-file";

/**
 * The reverse of `init`'s CLAUDE.md write. Deliberately exact-match only —
 * CLAUDE.md is hand-curated, high-trust prose a human reads and relies on,
 * so this refuses to guess at boundaries the way a heading-based or
 * marker-to-EOF strip would have to. It only acts when the live-regenerated
 * snippet (from the CURRENT config) appears verbatim in one of the two exact
 * shapes `init` can produce:
 *   - the file IS the snippet (created fresh by init, nothing else in it) —
 *     delete the file entirely rather than leave a bare header behind;
 *   - the snippet was appended to a pre-existing file — strip exactly that
 *     trailing substring, leaving everything before it untouched.
 * Anything else (hand-edited since, or init ran under a different config
 * than the one passed here) comes back "mismatch" — left completely alone
 * for a human to remove by hand.
 */
export function removeClaudeMdSnippet(root: string, cfg: ClaudeCodeMergeQueueConfig): RemoveSnippetResult {
  const path = join(root, "CLAUDE.md");
  if (!existsSync(path)) return "no-file";

  const content = readFileSync(path, "utf8");
  if (!content.includes(MARKER)) return "not-found";

  const snippet = claudeMdSnippet(cfg);
  const createdTemplate = `# Project instructions for Claude Code\n\n${snippet}`;

  if (content === createdTemplate) {
    rmSync(path);
    return "removed";
  }

  const appended = `\n${snippet}`;
  if (content.endsWith(appended)) {
    writeFileSync(path, content.slice(0, -appended.length).trimEnd() + "\n");
    return "removed";
  }

  return "mismatch";
}

/**
 * Regenerate the marker-delimited managed block in place. Replaces everything
 * from the opening MARKER through the end of the closing marker's HTML comment
 * with `snippet`, leaving all content before and after the block untouched.
 * This is what lets `init` re-sync a repo to the installed version without a
 * human editing CLAUDE.md by hand.
 *
 * Returns the updated content, or `null` when the block isn't a well-formed
 * delimited region (missing either marker, or the closing marker's `-->`) — in
 * which case the caller leaves the file alone rather than guessing boundaries.
 */
export function replaceClaudeMdSnippet(content: string, snippet: string): string | null {
  const start = content.indexOf(MARKER);
  if (start === -1) return null;
  const endMarkerAt = content.indexOf(END_MARKER_PREFIX, start);
  if (endMarkerAt === -1) return null;
  const close = content.indexOf("-->", endMarkerAt);
  if (close === -1) return null;
  const end = close + "-->".length;
  return content.slice(0, start) + snippet.trimEnd() + content.slice(end);
}
